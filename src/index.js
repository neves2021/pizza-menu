function Pizza({ pizzaObj }) {
  // if (pizzaObj.soldOut) return null;

  return (
    <li className={`pizza ${pizzaObj.soldOut ? "sold-out" : ""}`}>
      <img src={pizzaObj.photoName} alt={pizzaObj.name} />
      <div>
        <h3>{pizzaObj.name}</h3>
        <p>{pizzaObj.ingredients}</p>
        <span>{pizzaObj.soldOut ? "SEM STOCK" : pizzaObj.price}</span>
      </div>
    </li>
  );
}

function Footer() {
  const hour = new Date().getHours();
  const openHour = 21;
  const closeHour = 5;
  const isOpen = hour >= openHour || hour < closeHour;

  console.log(isOpen);

  // if (hour >= openHour && hour <= closeHour) alert("Estamos abertos!");
  // else alert("Estamos fechados."); //comentar ctrl + K + C

  return (
    <footer className="footer">
      {isOpen ? (
        <Order closeHour={closeHour} />
      ) : (
        <p>We're closed. Please comeback at {openHour}:00.</p>
      )}
    </footer>
  );
}

function Order({ closeHour }) {
  return (
    <div className="order">
      <p>We're open until {closeHour}:00.</p>
      <button className="btn">Order</button>
    </div>
  );
}

